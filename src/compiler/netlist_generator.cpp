std::string NetlistGenerator::generateSubcktInstance(const std::string& name,
                                                      const std::string& subcktName,
                                                      const std::vector<std::string>& nodes,
                                                      const std::map<std::string, double>& params) {
    std::ostringstream oss;
    oss << "X" << name << " ";
    
    for (const auto& node : nodes) {
        oss << node << " ";
    }
    
    oss << subcktName;
    
    for (const auto& [pname, pvalue] : params) {
        oss << " " << pname << "=" << valueToSpice(pvalue);
    }
    
    oss << "\n";
    return oss.str();
}

std::string NetlistGenerator::getInternalNode(const std::string& name) {
    auto it = m_nodeMap.find(name);
    if (it != m_nodeMap.end()) {
        return "n" + std::to_string(it->second);
    }
    
    int nodeNum = m_nodeCounter++;
    m_nodeMap[name] = nodeNum;
    return "n" + std::to_string(nodeNum);
}

