std::string NetlistGenerator::generateSubcktCard(const SubcktAST* subckt) {
    if (!subckt) return "";
    
    std::ostringstream oss;
    oss << ".SUBCKT " << subckt->getName();
    
    for (const auto& pin : subckt->getPins()) {
        oss << " " << pin;
    }
    oss << "\n";
    
    // Subcircuit body would be generated here
    // For now, just add placeholder
    oss << "* Subcircuit body\n";
    
    oss << ".ENDS " << subckt->getName() << "\n";
    return oss.str();
}

std::string NetlistGenerator::generateModelCard(const ModelAST* model) {
    if (!model) return "";
    
    std::ostringstream oss;
    oss << ".MODEL " << model->getName() << " " << model->getType() << " (\n";
    
    bool first = true;
    for (const auto& [name, value] : model->getParams()) {
        if (!first) oss << "\n";
        oss << "  " << name << "=" << valueToSpice(value);
        first = false;
    }
    
    oss << "\n)\n";
    return oss.str();
}

