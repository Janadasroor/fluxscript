NetlistGenerator::NetlistGenerator() : m_nodeCounter(100) {}

NetlistGenerator::~NetlistGenerator() {}

std::string NetlistGenerator::generateNetlist(
    const std::vector<std::unique_ptr<FunctionAST>>& functions,
    const std::vector<std::unique_ptr<SubcktAST>>& subckts,
    const std::vector<std::unique_ptr<ModelAST>>& models,
    const std::vector<std::unique_ptr<AnalysisDeclAST>>& analyses) {
    
    m_netlist.str("");
    m_netlist.clear();
    
    // Header
    m_netlist << "* FluxScript Generated Netlist\n";
    m_netlist << "* Generated by FluxScript Compiler\n";
    m_netlist << "\n";
    
    // Models
    for (const auto& model : models) {
        m_netlist << generateModelCard(model.get());
    }
    m_netlist << "\n";
    
    // Subcircuits
    for (const auto& subckt : subckts) {
        m_netlist << generateSubcktCard(subckt.get());
    }
    m_netlist << "\n";
    
    // Analysis
    for (const auto& analysis : analyses) {
        m_netlist << generateAnalysisCard(analysis.get());
    }
    
    m_netlist << "\n.END\n";
    
    return m_netlist.str();
}

