std::string NetlistGenerator::generateBSource(const std::string& name, 
                                               const std::vector<std::string>& nodes,
                                               const std::string& type,
                                               const std::string& expression) {
    if (nodes.size() < 2) return "";
    
    std::ostringstream oss;
    oss << "B" << name << " " << nodes[0] << " " << nodes[1] 
        << " " << type << "={" << expression << "}\n";
    return oss.str();
}

std::string NetlistGenerator::generateBVoltage(const std::string& name,
                                                const std::string& nPlus,
                                                const std::string& nMinus,
                                                const std::string& expression) {
    return generateBSource(name, {nPlus, nMinus}, "V", expression);
}

