std::string NetlistGenerator::valueToSpice(double value) {
    std::ostringstream oss;
    
    // Use engineering notation
    if (value >= 1e9) {
        oss << (value / 1e9) << "G";
    } else if (value >= 1e6) {
        oss << (value / 1e6) << "Meg";
    } else if (value >= 1e3) {
        oss << (value / 1e3) << "k";
    } else if (value >= 1) {
        oss << value;
    } else if (value >= 1e-3) {
        oss << (value * 1e3) << "m";
    } else if (value >= 1e-6) {
        oss << (value * 1e6) << "u";
    } else if (value >= 1e-9) {
        oss << (value * 1e9) << "n";
    } else if (value >= 1e-12) {
        oss << (value * 1e12) << "p";
    } else {
        oss << value;
    }
    
    return oss.str();
}

